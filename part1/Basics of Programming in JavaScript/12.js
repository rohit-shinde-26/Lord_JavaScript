// booleans & comparison operator 

// booleans 
// true, false 

let num1 = 7;
let num2 = "7";

console.log(num1<num2);

// == vs === 
console.log(num1 == num2);  // it checks value
console.log(num1 === num2); // it checks value as well as data type


// != vs !==
console.log(num1 != num2);  // can num1 and num2 different
console.log(num1 !== num2); // checks type also
